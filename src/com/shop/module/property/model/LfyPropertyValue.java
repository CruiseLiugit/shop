package com.shop.module.property.model;

public class LfyPropertyValue {
	
	private long id;
	
	private String pvCode;
	
	private String categoryPropertyCode;
	
	private String pvName;
	
	private String showName;
	
	private String pvtype;
	
	private int pvOrder;
	
	private String status;

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getPvCode() {
		return pvCode;
	}

	public void setPvCode(String pvCode) {
		this.pvCode = pvCode;
	}

	public String getCategoryPropertyCode() {
		return categoryPropertyCode;
	}

	public void setCategoryPropertyCode(String categoryPropertyCode) {
		this.categoryPropertyCode = categoryPropertyCode;
	}

	public String getPvName() {
		return pvName;
	}

	public void setPvName(String pvName) {
		this.pvName = pvName;
	}

	public String getShowName() {
		return showName;
	}

	public void setShowName(String showName) {
		this.showName = showName;
	}

	public String getPvtype() {
		return pvtype;
	}

	public void setPvtype(String pvtype) {
		this.pvtype = pvtype;
	}

	public int getPvOrder() {
		return pvOrder;
	}

	public void setPvOrder(int pvOrder) {
		this.pvOrder = pvOrder;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}
	
	
}
